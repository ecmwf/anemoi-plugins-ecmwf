from functools import lru_cache

import numpy as np


def grid_to_pl(grid: str) -> np.ndarray:
    """Convert a grid name to its pl (number of points per latitude row) array.

    - Octahedral (O): starts at 20, increments by 4 per row, N rows per hemisphere, mirrored.
    - Full (F): every row has 4*N points, 2*N rows total.
    - Classic reduced (N): irregular progression, looked up from a table. #TODO: Move into earthkit
      Only a subset of common N grids are included.

    Parameters
    ----------
    grid : str
        Grid name, e.g. "O1280", "N320", "F48".

    Returns
    -------
    numpy.ndarray
        Array of int64 with the number of points on each latitude row,
        ordered pole-to-pole.
    """

    prefix = grid[0].upper()
    n = int(grid[1:])

    if prefix == "O":
        nh = np.arange(20, 20 + 4 * n, 4, dtype=np.int64)
        return np.concatenate([nh, nh[::-1]])

    if prefix == "F":
        return np.full(2 * n, 4 * n, dtype=np.int64)

    if prefix == "N":
        return _classic_reduced_pl(n)

    raise ValueError(f"Unknown grid type: {grid}")


