def _classic_reduced_pl(n):
    """Return the full pole-to-pole pl array for a classic reduced Gaussian grid."""

    if n not in _CLASSIC_REDUCED_NH:
        available = sorted(_CLASSIC_REDUCED_NH.keys())
        raise ValueError(
            f"Classic reduced Gaussian N{n} not in lookup table. "
            f"Available: {', '.join('N' + str(k) for k in available)}. "
            # f"Use earthkit.geo.reduced_gg('N{n}').pl for other resolutions."
        )
    nh = np.array(_CLASSIC_REDUCED_NH[n], dtype=np.int64)
    if len(nh) != n:
        raise ValueError(f"N{n} lookup has {len(nh)} rows, expected {n}")
    return np.concatenate([nh, nh[::-1]])
