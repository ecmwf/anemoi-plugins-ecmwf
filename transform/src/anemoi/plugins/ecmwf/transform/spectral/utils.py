def nspec_from_trunc(trunc: int) -> int:
    """Number of real spectral coefficients for triangular truncation T."""
    return (trunc + 1) * (trunc + 2)


def trunc_from_nspec(nspec: int) -> int:
    """Derive truncation T from spectral coefficient count (T+1)*(T+2)."""
    trunc = int((-3 + (9 + 4 * nspec) ** 0.5) / 2)
    if (trunc + 1) * (trunc + 2) != nspec:
        raise ValueError(
            f"Cannot derive truncation from spectral size {nspec}; " "expected (T+1)*(T+2) for some integer T"
        )
    return trunc


@lru_cache(maxsize=32)
def _truncation_indices(t_in: int, t_out: int) -> np.ndarray:
    """Build an index array that extracts T_out coefficients from a T_in spectrum.

    GRIB triangular spectral ordering (real/imaginary pairs):
      m=0: (Re(0,0), Im(0,0)), (Re(0,1), Im(0,1)), ..., (Re(0,T), Im(0,T))
      m=1: (Re(1,1), Im(1,1)), ..., (Re(1,T), Im(1,T))
      ...
      m=T: (Re(T,T), Im(T,T))

    For wavenumber m, there are 2*(T - m + 1) real values starting at
    offset(m) = sum_{k=0}^{m-1} 2*(T - k + 1).

    To truncate to T_out we keep, for each m <= T_out, the first
    2*(T_out - m + 1) values from wavenumber m.
    """
    indices = []
    src_offset = 0
    for m in range(t_out + 1):
        n_keep = 2 * (t_out - m + 1)
        indices.append(np.arange(src_offset, src_offset + n_keep))
        src_offset += 2 * (t_in - m + 1)
    return np.concatenate(indices)


def truncate_spectral(data: np.ndarray, t_out: int) -> np.ndarray:
    """Truncate spectral coefficient array(s) from T_in to T_out.

    Parameters
    ----------
    data : np.ndarray
        Spectral data with the last axis being spectral coefficients
        in GRIB triangular ordering.  Shape ``(..., nspec_in)``.
    t_out : int
        Target truncation.

    Returns
    -------
    np.ndarray
        Truncated spectral data, shape ``(..., (t_out+1)*(t_out+2))``.
    """
    nspec_in = data.shape[-1]
    t_in = trunc_from_nspec(nspec_in)

    if t_out >= t_in:
        return data
    if t_out < 0:
        raise ValueError(f"Target truncation must be >= 0, got {t_out}")

    idx = _truncation_indices(t_in, t_out)
    return data[..., idx]


def grid_to_trunc(grid: str) -> int:
    """Convert a grid name to its corresponding truncation value.

    - Octahedral (O): T = N - 1 (cubic truncation, TCo)
    - Classic reduced (N) and Full (F): T = 2N - 1 (linear truncation, TL)

    Parameters
    ----------
    grid : str
        The name of the grid.

    Returns
    -------
    int
        The truncation value corresponding to the grid.
    """
    prefix = grid[0].upper()

    if prefix == "O":
        return int(grid[1:]) - 1
    elif prefix == "N" or prefix == "F":
        return 2 * int(grid[1:]) - 1
    elif prefix == "T":
        return int(grid[1:])
    else:
        raise ValueError(f"Unknown grid type: {grid}")


